package fuctura.model;

public class Time {
	private String nome;
	private Integer qtdTitulos;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public Integer getQtdTitulos() {
		return qtdTitulos;
	}

	public void setQtdTitulos(Integer qtdTitulos) {
		this.qtdTitulos = qtdTitulos;
	}
}
